          <div class="collapsible-header">
            <h4>La diferencia en Tatuaje convencional y Micropigmentación</h4>
          </div>
          <div class="collapsible-body">
            Los tatuajes convencionales son manchas o diseños producidos por sustancias coloreadas y que no se eliminan  del organismo con facilidad. Sus objetos cortantes o punzo-cortantes.  Se implanta en cualquier área del cuerpo seleccionando por regla general zonas en donde la piel es gruesa. Las máquinas para tatuaje corporal mueven largas agujas de manera trepidatoria para hacer el depósito cromático en el espesor papilo-reticular de la dermis. El procedimiento es doloroso, traumatizante y tan profundamente implantado, que se presume que dura para siempre. Quien hace tatuaje corporal busca plasmar sus dibujos artísticos por el trazo y el color en grandes áreas de la piel. <br><br>
            La Micropigmentación es un proceso diferente que se realiza en la delicada piel de la cara. Siempre buscará embellecer  y corregir de manera remodelados en caso de que el gusto o la moda cambien. Igualmente por acción de la edad, cuando aparezca la flacidez o arrugas cerca de las zonas micropigmentadas. La micropigmentación se aplica mediante un dermógrafo ligero que emplea agujas cortas que se mueven de forma rotatoria, trepidatoria, depositando pigmento justamente debajo del estrato germinativo, en la capa papilar dérmica. Estos aparatos brindan precisión en los detalles finos y facilitan la minuciosidad que es cualidad del Técnico en Micropigmentación quien además es un artista del maquillaje y en especial se considera un vitalista. Los Técnicos en Micropigmentación buscarán corregir, simetrizar y armonizar  los ojos, cejas, y labios  de acuerdo a la personalidad del solicitante.  
