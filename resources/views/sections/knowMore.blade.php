              <h4> ¿Qué es la micropigmentación?</h4>
          </div>
          <div class="collapsible-body">
            <span>La Dermopigmetación o micropigmentación es un procedimiento utilizado desde la antigüedad a través de la cual se colocaban partículas de diversos materiales debajo de la superficie cutánea con el fin de obtener dibujos "permanentes".<br><br> 
            Hoy en día la micropigmentación es un procedimiento de implante de pigmentos en la dermis. Con efectos de afinación de rasgos estéticos. <br><br> 
            Es un microtatuaje de belleza. <br><br>  
            Se sugiere generalmente para: <br><br> 
            
            La corrección simetría, armonía de ojos, cejas y labios. <br>    
            Según la personalidad del cliente.
